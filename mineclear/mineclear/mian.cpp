#define _CRT_SECURE_NO_WARNINGS

#include"mine.h"

void menu()
{
	printf("******************************\n");
	printf("******    1. play      *******\n");
	printf("******    0. exit      *******\n");
	printf("******************************\n");
}

void game()
{
	char mine[ROWS][COLS] = { 0 };//存放布置好的雷的信息
	char show[ROWS][COLS] = { 0 };//存放排查出的雷的信息
	//初始化棋盘
	InitBoard(mine,'0');//'0'
	InitBoard(show,'*');//'*'

	//打印一下棋盘
	PrintBoard(show);

	//布置雷
	SetMineBoard(mine);
	int win = 1;
	while (win)
	{
		//排查雷
		SeekMine(mine, show);
		PrintBoard(show);
		win = Win(mine, show);
	}
	
}

int main()
{
	srand((unsigned int)time(NULL));
	int input = 0;
	do
	{
		menu();
		printf("请选择:>");
		scanf("%d", &input);
		switch (input)
		{
		case 0:
			printf("退出游戏\n");
			break;
		case 1:
			game();
			break;
		default:
			printf("选择错误，重新选择\n");
			break;
		}
	} while (input);
	return 0;
}